, ch1.PRECO_INI_ANO_MEDIO = ch2.PRECO_MED
WHERE ch1.PRAZO_TERMO = md.PRAZO_TERMO
AND ch1.COD_PAPEL = md.COD_PAPEL
AND ch1.COD_BDI = md.COD_BDI
AND md.data_pregao = ch2.data_pregao
